def describeletter():

	ltr = raw_input("Enter a letter: ")

	if ltr == 'a':
		print(ltr, "is a vowel")
	elif ltr == 'e':
		print(ltr, "is a vowel")
	elif ltr == 'i':
		print(ltr, "is a vowel")
	elif ltr == 'o':
		print(ltr, "is a vowel")
	elif ltr == 'u':
		print(ltr, "is a vowel")
	elif ltr == 'y':
		print(ltr, "is sometimes a vowel and sometimes a consonant")              
	else:
		print(ltr, "is a consonant")

